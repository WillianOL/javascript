var n1 = 5
for(var n2 = 1; n2 <= n1; n2++){ //O for contem o inicio, condição e incremento
    if(n2 == n1 ){
        console.log(`Agora sim, o primeiro ${n1} é igual ao segundo ${n2}`) //Quando os dois números forem iguais, mostrar essa mensagem
    } else{
        console.log(`O premeiro número ${n1} não é igual ao segundo ${n2}`); //Enquanto os dois números forem diferentes, mostrar essa mensagem.
    }
}
